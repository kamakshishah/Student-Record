class Student
{
	private int erno;
	private String ename;
	private int emarks;
	
	public int getErno()
	{
		return erno;
	}
	
	public void setErno(int erno)
	{
		this.erno=erno;
	}
	public String getEname()
	{
		return ename;
	}
	
	public void setEname(String ename)
	{
		this.ename=ename;
	}
	public int getEmarks()
	{
		return emarks;
	}
	
	public void setEmarks(int emarks)
	{
		this.emarks=emarks;
	}
	
}
	